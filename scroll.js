// z index change for the 'our services' section interactions

document.addEventListener('DOMContentLoaded', () => {
 document.querySelectorAll('#ci-trigger').forEach(trigger => {
  trigger.addEventListener('mouseover', function(){ 
    document.querySelectorAll('#ci').forEach(target => target.classList.add('z-index-high')); 
  });
 });     
 document.querySelectorAll('#ci-trigger').forEach(trigger => {
  trigger.addEventListener('mouseout', function(){ 
    document.querySelectorAll('#ci').forEach(target => target.classList.remove('z-index-high')); 
  });
 }); 
});
